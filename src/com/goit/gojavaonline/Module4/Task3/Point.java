package com.goit.gojavaonline.module4.task3;




public class Point {

    private double X;
    private double Y;




    public double getX() {
        return X;
    }

    public void setX(double X) {
        this.X = X;
    }

    public double getY() {
        return Y;
    }

    public void setY(double Y) {
        this.Y = Y;
    }

    public Point(double X, double Y) {
            this.X = X;
            this.Y = Y;
    }






}
